public class Telephone {

    private String color;
    private String model;
    private String type;
    private String typeCommunications;


    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getTypeCommunications() {
        return typeCommunications;
    }

    public void setTypeCommunications(String typeCommunications) {
        this.typeCommunications = typeCommunications;
    }
}

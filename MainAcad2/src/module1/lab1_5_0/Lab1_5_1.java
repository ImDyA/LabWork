package module1.lab1_5_0;

public class Lab1_5_1 {
    public static void main(String[] args) {
        int a, b, t, y;
        for (a=1;a<=10;a++){
            for (b=a;b>0;b--){
                System.out.print(b + " ");
            }
            System.out.println();
        }
        for (t=9;t>0;t--){
            for (y=t;y>0;y--){
                System.out.print(y + " ");
            }
            System.out.println();
        }
    }
}
